@dataclass(frozen=True)
class HubSensorDef:
    key: str
    name: str
    device: str
    device_class: SensorDeviceClass | None = None
    state_class: SensorStateClass | None = None
    unit: str | None = None
    icon: str | None = None
    # primary: unique_id patterns (see module docstring)
    matchers: dict[str, list[str]] = field(default_factory=dict)
    # fallback: object_id patterns (exact, then "*_pattern" suffix)
    fallbacks: dict[str, list[str]] = field(default_factory=dict)


def _power(key, name, device, matchers, fallbacks=None, icon=None):
    return HubSensorDef(
        key=key, name=name, device=device,
        device_class=SensorDeviceClass.POWER,
        state_class=SensorStateClass.MEASUREMENT, unit=UnitOfPower.WATT,
        icon=icon, matchers=matchers, fallbacks=fallbacks or {},
    )


def _energy(key, name, device, matchers, fallbacks=None, icon=None):
    return HubSensorDef(
        key=key, name=name, device=device,
        device_class=SensorDeviceClass.ENERGY,
        state_class=SensorStateClass.TOTAL_INCREASING,
        unit=UnitOfEnergy.KILO_WATT_HOUR, icon=icon,
        matchers=matchers, fallbacks=fallbacks or {},
    )


def _voltage(key, name, device, matchers, fallbacks=None):
    return HubSensorDef(
        key=key, name=name, device=device,
        device_class=SensorDeviceClass.VOLTAGE,
        state_class=SensorStateClass.MEASUREMENT,
        unit=UnitOfElectricPotential.VOLT,
        matchers=matchers, fallbacks=fallbacks or {},
    )


def _current(key, name, device, matchers, fallbacks=None):
    return HubSensorDef(
        key=key, name=name, device=device,
        device_class=SensorDeviceClass.CURRENT,
        state_class=SensorStateClass.MEASUREMENT,
        unit=UnitOfElectricCurrent.AMPERE,
        matchers=matchers, fallbacks=fallbacks or {},
    )


def _percent(key, name, device, matchers, fallbacks=None, device_class=None):
    return HubSensorDef(
        key=key, name=name, device=device, device_class=device_class,
        state_class=SensorStateClass.MEASUREMENT, unit=PERCENTAGE,
        icon=None if device_class else "mdi:percent",
        matchers=matchers, fallbacks=fallbacks or {},
    )


def _text(key, name, device, matchers, fallbacks=None, icon=None):
    return HubSensorDef(
        key=key, name=name, device=device, icon=icon,
        matchers=matchers, fallbacks=fallbacks or {},
    )


