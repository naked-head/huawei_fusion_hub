"""Canonical sensor definitions and per-source matchers.

Matching is language-independent: the primary layer tests patterns
against the entity registry unique_id, which is generated by source
code and never localized. A fallback layer tests object_ids for older
source versions with different unique_id schemes.

Pattern syntax (per source, ordered):
- "pattern"          -> unique_id equals pattern or ends with "_pattern"
                        or "-pattern" (case-insensitive)
- "Model:pattern"    -> same, but only on entities whose device model
                        equals Model (used for FusionSolarPlus, which
                        reuses numeric signal ids across device types
                        and sets model = "Inverter" / "Battery" /
                        "Power Sensor" / "Plant")

Verified sources:
- huawei_solar: unique_id = "{serial}_{register_name}"; register names
  from huawei-solar lib register_names.py (stable English strings).
- fusion_solar: unique_id = "fusion_solar-{id}-{sensor_id}".
- fusionsolarplus: unique_id = "{device_id}_{signal_id}" (numeric
  FusionSolar API signal ids) for devices, "{device_id}_{key}" for
  plant. Device model = device_type (hardcoded English).
Values cross-checked against a real installation dump (2026-07-06).
"""
